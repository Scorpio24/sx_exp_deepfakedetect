class CA_S3D_v3(nn.Module):
    def __init__(self, num_class, SRM_net):
        super(CA_S3D_v3, self).__init__()

        self.SRM_net = SRM_net
        if SRM_net == 'yes':
            input_channels = 30
        else:
            input_channels = 3

        self.SRM = HPF()
        self.base = nn.Sequential(# input:bs*(3/30) *20*224*224
            SepConv3d(input_channels, 64, kernel_size=7, stride=2, padding=3),#out:bs*64*10*112*112
            nn.MaxPool3d(kernel_size=(1,3,3), stride=(1,2,2), padding=(0,1,1)),#out:bs*64*10*56*56
            BasicConv3d(64, 64, kernel_size=1, stride=1),#out:bs*64*10*56*56
            SepConv3d(64, 192, kernel_size=3, stride=1, padding=1),#out:bs*192*10*56*56
            # MSCAN_half(192,1), #v2
            nn.MaxPool3d(kernel_size=(1,3,3), stride=(1,2,2), padding=(0,1,1)),#out:bs*192*10*28*28
            # MSCAN_half(192, 1),#out:bs*192*10*28*28 v1
            Mixed_3b(),#out:bs*256*10*28*28
            ContextBlock3d(inplanes=256, ratio=1./16.,pooling_type='avg'), #v3
            Mixed_3c(),#out:bs*480*10*28*28
            nn.MaxPool3d(kernel_size=(3,3,3), stride=(2,2,2), padding=(1,1,1)),#out:bs*480*5*14*14
            # MSCAN_half(480, 1),#out:bs*192*10*28*28 v1
            Mixed_4b(),#out:bs*512*5*14*14
            ContextBlock3d(inplanes=512, ratio=1./16.,pooling_type='avg'), #v3
            Mixed_4c(),#out:bs*512*5*14*14
            ContextBlock3d(inplanes=512, ratio=1./16.,pooling_type='avg'), #v3
            Mixed_4d(),#out:bs*512*5*14*14
            ContextBlock3d(inplanes=512, ratio=1./16.,pooling_type='avg'), #v3
            Mixed_4e(),#out:bs*528*5*14*14
            ContextBlock3d(inplanes=528, ratio=1./16.,pooling_type='avg'), #v3
            Mixed_4f(),#out:bs*832*5*14*14
            nn.MaxPool3d(kernel_size=(2,2,2), stride=(2,2,2), padding=(0,0,0)),#out:bs*832*2*7*7
            Mixed_5b(),#out:bs*832*2*7*7
            ContextBlock3d(inplanes=832, ratio=1./16.,pooling_type='avg'), #v3
            Mixed_5c(),#out:bs*1024*2*7*7
        )
        self.fc = nn.Sequential(nn.Conv3d(1024, num_class, kernel_size=1, stride=1, bias=True),)

    def forward(self, x):
        if self.SRM_net == 'yes':
            y = self.SRM(x)
        else:
            y = x
        y = self.base(y)
        y = F.avg_pool3d(y, (2, y.size(3), y.size(4)), stride=1)
        y = self.fc(y)
        y = y.view(y.size(0), y.size(1), y.size(2))
        logits = torch.mean(y, 2)

        return logits

class BasicConv3d(nn.Module):
    def __init__(self, in_planes, out_planes, kernel_size, stride, padding=0):
        super(BasicConv3d, self).__init__()
        self.conv = nn.Conv3d(in_planes, out_planes, kernel_size=kernel_size, stride=stride, padding=padding, bias=False)
        self.bn = nn.BatchNorm3d(out_planes, eps=1e-3, momentum=0.001, affine=True)
        self.relu = nn.ReLU()

    def forward(self, x):
        x = self.conv(x)
        x = self.bn(x)
        x = self.relu(x)
        return x

class SepConv3d(nn.Module):
    def __init__(self, in_planes, out_planes, kernel_size, stride, padding=0):
        super(SepConv3d, self).__init__()
        self.conv_s = nn.Conv3d(in_planes, out_planes, kernel_size=(1,kernel_size,kernel_size), stride=(1,stride,stride), padding=(0,padding,padding), bias=False)
        self.bn_s = nn.BatchNorm3d(out_planes, eps=1e-3, momentum=0.001, affine=True)
        self.relu_s = nn.ReLU()

        self.conv_t = nn.Conv3d(out_planes, out_planes, kernel_size=(kernel_size,1,1), stride=(stride,1,1), padding=(padding,0,0), bias=False)
        self.bn_t = nn.BatchNorm3d(out_planes, eps=1e-3, momentum=0.001, affine=True)
        self.relu_t = nn.ReLU()

    def forward(self, x):
        x = self.conv_s(x)
        x = self.bn_s(x)
        x = self.relu_s(x)

        x = self.conv_t(x)
        x = self.bn_t(x)
        x = self.relu_t(x)
        return x

class Mixed_3b(nn.Module):
    def __init__(self):
        super(Mixed_3b, self).__init__()

        self.branch0 = nn.Sequential(
            BasicConv3d(192, 64, kernel_size=1, stride=1),
        )
        self.branch1 = nn.Sequential(
            BasicConv3d(192, 96, kernel_size=1, stride=1),
            SepConv3d(96, 128, kernel_size=3, stride=1, padding=1),
        )
        self.branch2 = nn.Sequential(
            BasicConv3d(192, 16, kernel_size=1, stride=1),
            SepConv3d(16, 32, kernel_size=3, stride=1, padding=1),
        )
        self.branch3 = nn.Sequential(
            nn.MaxPool3d(kernel_size=(3,3,3), stride=1, padding=1),
            BasicConv3d(192, 32, kernel_size=1, stride=1),
        )

    def forward(self, x):
        x0 = self.branch0(x)
        x1 = self.branch1(x)
        x2 = self.branch2(x)
        x3 = self.branch3(x)
        out = torch.cat((x0, x1, x2, x3), 1)

        return out


